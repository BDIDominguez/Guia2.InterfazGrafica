    private int buscarCodigo(){
        int codigo = 1;
        boolean vEcontrado = true;
        while (vEcontrado){
            vEcontrado = false;
            for (Articulos producto : productos) {
                if (producto.getCodigo() == codigo){
                    vEcontrado = true;
                    break;
                }
            }
            if (vEcontrado){
                codigo++;
            }
        }
        return codigo;
    }
    
    private boolean existeCodigo(int codigo){
        boolean vEcontrado = false;
        for (Articulos productos : productos){
            if (productos.getCodigo() == codigo){
                vEcontrado = true;
                break;
            }
        }
        return vEcontrado;
    }
    private Articulos crearArticulo(){
        int codigo = Integer.parseInt(jtxCodigo.getText());
        double precio = Double.parseDouble(jtxPrecio.getText());
        double stock = Double.parseDouble(jtxStock.getText());
        //System.out.println(jcbCategoria.getSelectedItem());
        Categorias b = Categorias.valueOf(jcbCategoria.getSelectedItem().toString());
        Articulos a = new Articulos(codigo, jtxDescripcion.getText(), precio, stock, b);
        return a;
    }
    private void accionBotonBuscar(){
        if (!jtxCodigo.getText().equals("0000") || !jtxCodigo.getText().isEmpty()){
            int vCodigo = Integer.parseInt(jtxCodigo.getText());
            Articulos vBuscado = null;
            for (Articulos producto : productos) {
                if (producto.getCodigo() == vCodigo){
                    vBuscado = producto;
                    break;
                }
            }
            if (vBuscado != null){ // Asignar datos a los elementos
                jtxCodigo.setText(vBuscado.getCodigo() + "");
                jtxDescripcion.setText(vBuscado.getDescripcion());
                jtxPrecio.setText(vBuscado.getPrecio() + "");
                jcbCategoria.setSelectedItem(vBuscado.getRubro().toString());
                jtxStock.setText(vBuscado.getStock() + "");
            }else{
                JOptionPane.showMessageDialog(this, "Ese codigo no existe");
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Ingrese un codigo valido");
        }
    }

