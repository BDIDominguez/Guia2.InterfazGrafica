    private void jbtEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEliminarActionPerformed
        if (!jtxCodigo.getText().equals("0000") || !jtxCodigo.getText().isEmpty()){
            int vResp = JOptionPane.showConfirmDialog(this, "Seguro de eliminar el Articulo: " + jtxCodigo.getText() + " - " + jtxDescripcion.getText());
            // en vResp se guarda la respuest adel jption que puede ser 0 1 2 en el orden que aparece los botones en el mismo
            if (vResp == 0){
                Articulos b = crearArticulo();
                boolean vr = productos.remove(b);
                if (vr){
                    b = productos.first();
                    jtxCodigo.setText(b.getCodigo() + "");
                    accionBotonBuscar();
                }else{
                    JOptionPane.showMessageDialog(this, "No se pudo eliminar el articulo");
                }
            }
        }
    }//GEN-LAST:event_jbtEliminarActionPerformed

